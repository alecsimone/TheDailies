	<p class="proposalBody">If there were, how would we even go about figuring out what it is? Do we just ask Gibbs? Or JohnnyBoi? Or Turbopolsa? Just with those three people, I bet most days they'd disagree. Do we have them create a formula to calculate the best play, balancing like, how high the stakes are, how crazy the mechanics look, how coordinated the team is, and how smart the strategy seems? Do you think they could even agree on that?</p>

	<p class="proposalBody"><span class="highlight">You know what I'm getting at. It's subjective, there is no definitive Best Play of the Day.</span> Everyone likes different things and (trust me) they often disagree. <span class="highlight">And yet every day, we at the Dailies pick one play as the best, hoist it up on our shoulders, and give it a prize. Why do we do this?</span></p>

	<p class="proposalBody">Honestly, the historic answer is that it's something of a joke mixed with a marketing ploy. Give people a reason to share my tweets, and then laugh at the ridiculousness of the whole thing by letting the number of Twitter likes determine who wins. Preposterous!</p>

	<p class="proposalBody">The part of the process we put painstaking care into is deciding which plays get nominated and posted on twitter. The idea was that we couldn't possibly pick The Single Correct Best Play, but we could make a list that almost definitely had the best play on it and let fate decide who won. So that's what we did.</p>

	<p class="proposalBody"><span class="highlight">I think it's time to grow past that. I think we can raise some more money for the prize and then spread it across all of our favorite plays.</span> I don't think the Dailies should be this elitist gatekeeper of what is and isn't good. I think it should be a place you can come together with a bunch of other people who like this game to find stuff you love and help it grow. And also, from time to time, to teach some idiots that they're wrong to like what they like. And maybe occasionally have some cool person show you a new way to appreciate something.</p>
	
	<p class="proposalBody"><span class="highlight">So I'm proposing that at the end of every day's stream we pick winners instead of nominees.</span> We could pick as many or as few as we like; some days there really is one play that everyone agrees is the best one. But more often we'd argue for a while about which ones sucked and which ones were awesome and once we got to the point where it was clear people weren't budging and there was nothing more to be said, we'd take everybody's favorite plays and split the prize between them.</p>
	
	<p class="proposalBody">There are a lot of details that would have to be tweaked and calibrated as we went along. I'm not pretending I have a completely perfect plan as to how we'd implement this. I'm definitely open to hearing suggestions on it though, and <span class="highlight">if we do decide to make the change this is definitely just one step in a never-ending pursuit of improvement.</span></p>
	
	<p class="proposalBody"><span class="highlight">But what I'm really asking today is if you agree with the principle I'm proposing.</span> Is it ridiculous to pick a single best play of the day? Or is it more exciting and interesting that way? Some people have said it would make arguing over who to pick less fun, since everybody gets a trophy anyway. How do you feel about that? Are you worried about how you'd be involved since the stream is at a really inconvenient time for you? I have some ideas about how to gather people's thoughts throughout the day, but this is definitely one of my big concerns.</p>
	
	<p class="proposalBody">And I mean, on some level the concept of a singular winner, one champion who towers over everyone else, is just more fucking satisfying than picking a few joint-co-special-participants. If more people get the award, that makes it less cool. Do you think that might ruin it?</p>
	
	<p class="proposalBody"><span class="highlight">Next Wednesday, the 30th, I'm going to host a stream where we can discuss this question and come to a final decision.</span> I'm leaving this page open until then to collect as many thoughts and opinions as you guys can come up with. Even if you just want to drop an emoji or something, I'd really love to know how people feel about this.</p>
	
	<p class="proposalBody">I don't want the Dailies to be something I do, I want it to be something the community does. I want us to decide together what we care about and want to support, and in that spirit I think we need to decide together how we want to make the decisions.</p>
	
